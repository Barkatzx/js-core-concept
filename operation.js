// Advance Mathematical Operators
// Variable Declaration
var tomatoPrice = 25;
// Variable Value Update
tomatoPrice = 30;
// Add Something To The Previous Value
var price1 = 31;
var price2 = 10;
// Set a New Value
price1 = 35;
// Add Some Value To The Existing Value
price1 = price1 + 10;
console.log(price1);
// -------------------------------------------------
// Addition To The Same Variable in (Short-Hand)
// += Means Adding Some Value To The Existing Value
price1 += 10;
// -= Means Diducted Some Value To The Existing Value:
price2 = price2 - 5;
// In Short-Hand:
price2 -= 5;
// *= Means Multiplication Some Value To The Existing Value:
price1 = price1 * 6;
// In Short-Hand
price1 *= 6;
// /= Means Division Some Value To The Existing Value
price1 /= 6;
// ---------------------------------------------------------
