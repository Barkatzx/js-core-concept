// Simple Mathematical Operators:
var onionPrice = 200;
var eggPrice = 100;
// (Addition (+), Subtraction (-), Multiplication (*), Division (/), Modulus (%))
var totalPrice = onionPrice + eggPrice
var totalPrice = onionPrice * eggPrice
var totalPrice = onionPrice - eggPrice
var totalPrice = onionPrice / eggPrice
var totalPrice = onionPrice % eggPrice
console.log(totalPrice);

